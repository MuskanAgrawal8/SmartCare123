// =======================
// UPDATE DOCTOR PROFILE IMAGE
// =======================
const updateDoctorImage = async (req, res) => {
  try {
    const doctorId = req.params.id;
    const imageFile = req.file;

    if (!imageFile) {
      return res.json({ success: false, message: "No image file provided" });
    }

    // Upload new image to Cloudinary
    const uploadedImage = await cloudinary.uploader.upload(imageFile.path, { resource_type: "image" });
    const imageUrl = uploadedImage.secure_url;

    // Update doctor image
    const updatedDoctor = await doctorModel.findByIdAndUpdate(
      doctorId,
      { image: imageUrl },
      { new: true }
    );

    if (!updatedDoctor) {
      return res.json({ success: false, message: "Doctor not found" });
    }

    res.json({ success: true, message: "Doctor image updated successfully", doctor: updatedDoctor });
  } catch (error) {
    console.log(error);
    res.json({ success: false, message: error.message });
  }
};

